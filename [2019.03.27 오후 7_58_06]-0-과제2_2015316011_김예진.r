  for(j in 1:i){ #for 반복구문은 1에서 i의 횟수만큼 다음을 반복 실행하라 (j는 별의 개수) 
      cat("*") #위의 for 반복구문에 따라 *을 출력하라
    }
  cat("\n") #위의 for 반복구문에 따라 줄 바꿈을 출력하라
}

