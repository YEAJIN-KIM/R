#week4-3 1단-20단 구구단 출력 (while)
i<-1 #객체 i에 1을 할당하라
j<-1 #객체 j에 1을 할당하라
while (i<=20) { #while 반복구문을 i가 20이하의 범위일 경우 다음을 반복 실행하라
  while(j<=9){ #while 반복구문을 j가 9이하의 범위일 경우 다음을 반복 실행하라
    cat(i,"*",j,"=",i*j,"\n") #구구단의 결과값을 i*j= _와 같은 형태로 출력하며 곱해지는 숫자 j가 바뀔시 줄 바꿈을 실행하라
    j<-j+1 #j의 값을 j+1한 값으로 할당하라 (구구단이 1에서 9까지 곱해지도록)
  }
  i<-i+1 #i의 값을 i+1한 값으로 할당하라
  j<-1 #j의 값이 9에서 멈춤으로 다시 1로 재정의 해줘라
  cat("\n") #단이 바뀔시 줄 바꿈을 출력하라
}

#week4-4 9단-1단 구구단 출력 (while)
i<-9 #객체 i에 9을 할당하라
