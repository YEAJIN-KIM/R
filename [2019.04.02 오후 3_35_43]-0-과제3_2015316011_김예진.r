  if(i%%3==0){ #조건문 if는 i를 3으로 나눌시 나머지가 0일시 다음의 구문을 실행하라 (3의 배수일 경우)
    c<-c+i #객체 c의 값을 c+i로 할당하라 (3의 배수일 경우 더해서 c가 3의 배수의 총합이 됨)
  }

} 
cat(c) #3의 배수의 합인 c를 출력하라

#week4-7 100~1000 3으로 나누어 떨어지는 숫자의 개수와 합 
d<-0 #객체 d에 0을 할당하라
e<-0 #객체 e에 0을 할당하라
for(i in 100:1000){ #for 반복구문은 100에서 1000까지의 횟수만큼 다음을 반복 실행하라 (i는 100에서 1000까지의 수)
  if(i%%3==0){ #조건문 if는 i를 3으로 나눌시 나머지가 0일시 다음의 구문을 실행하라 (3으로 나누어 떨어지는 수)
    d<-d+i  #객체  d의 값을 d+i로 할당하라 (3으로 나누어 떨어지는 수 일 경우 더해서 d가 총합이 됨)
    e<-e+1 #객체 e의 값을 e+1로 할당하라 (3으로 나누어 떨어지는 수일 경우 그 수의 개수를 e라 지정하고 그값을 할당함)
  }
} 
cat(d) #3으로 나누어 떨어지는 수의 합인 d를 출력하라
cat(e) #3으로 나누어 떨어지는 수의 총 개수인 e를 출력하라 