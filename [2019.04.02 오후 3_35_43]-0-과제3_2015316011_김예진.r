while (i>=1) { #while 반복구문을 i가 1이상의 범위일 경우 다음을 반복 실행하라
  while(j<=9){ #while 반복구문을 j가 9이하의 범위일 경우 다음을 반복 실행하라
    cat(i,"*",j,"=",i*j,"\n") #구구단의 결과값을 i*j= _와 같은 형태로 출력하며 곱해지는 숫자 j가 바뀔시 줄 바꿈을 실행하라
    j<-j+1 #j의 값을 j+1한 값으로 할당하라 (구구단이 1에서 9까지 곱해지도록)
  }
  i<-i-1 #i의 값을 i-1한 값으로 할당하라 (구구단이 9에서 1단까지 출력되도록)
  j<-1 #j의 값이 9에서 멈춤으로 다시 1로 재정의 해줘라
  cat("\n") #단이 바뀔시 줄 바꿈을 출력하라
}

#week4-5 1~100 홀수의 합과 짝수의 합
a<-0 #객체 a에 0을 할당하라
b<-0 #객체 b에 0을 할당하라
for(i in 1:100){ #for 반복구문은 1에서 100까지의 횟수만큼 다음을 반복 실행하라 (i는 1에서 100까지의 수)
  if(i%%2==0){ #조건문 if는 i를 2으로 나눌시 나머지가 0일시 다음의 구문을 실행하라 (짝수일 경우)
    a<-a+i #객체 a의 값을 a+i로 할당하라 (짝수일 경우 더해서 a가 짝수의 총합이 됨)
  }
  else{ #만약 i를 2로 나눌시 나머지가 0이 아닌 경우 다음의 구문을 실행하라(홀수일 경우)
    b<-b+i #객체 b의 값을 b+i로 할당하라 (홀수일 경우 더해서 b가 홀수의 총합이 됨)
  }
} 
cat("짝수의 합",a,"홀수의 합",b ) # 짝수의 합 a 홀수의 합 b 형식으로 다음의 값을 출력하라


#week4-6 50~200 3의 배수의 합
c<-0 #객체 c에 0을 할당하라
