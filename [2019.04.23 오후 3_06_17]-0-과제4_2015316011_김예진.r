#week5-6 10개의 정수를 입력받아 평균, 중앙값, 표준편차, 사분위범위, 최대값, 최소값 출력
e <- function(x1,x2,x3,x4,x5,x6,x7,x8,x9,x10){ #10개의 정수를 받는 함수 e를 생성
  x <- c(x1,x2,x3,x4,x5,x6,x7,x8,x9,x10) 
  Mean<-mean(x) #x에 해당하는 평균을 구해서 Mean에 할당하라
  Median<-median(x) #x에 해당하는 중앙값을 구해서 Median에 할당하라
  Sd<-sd(x) #x에 해당하는 표준편차를 구해서 Sd에 할당하라
  iqr<-IQR(x) #x에 해당하는 사분위 범위를 구해서 iqr에 할당하라
  Max<-max(x) #x에 해당하는 최대값을 구해서 Max에 할당하라
  Min<-min(x) #x에 해당하는 최소값을 구해서 Min에 할당하라
  cat("평균:",Mean,"\n","중앙값:",Median,"\n","표준편차:",Sd,"\n","사분위범위:",iqr,"\n","최대값:",Max,"\n","최소값:",Min,"\n") #다음과 같이 값을 출력하라
}
