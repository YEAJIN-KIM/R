#week5-4 1보다 큰 정수를 입력받아  1부터 x 까지 숫자 중 소수를 출력
c<-function(x){ #x를 받는 함수 c를 생성
  for(i in 1:x){ #1부터 x값의 범위까지 i에 할당
    n<-0 #i%%j 일시 나머지가 0 이되는 수(소수)를 셀 수 있는 n 생성
    for (j in 1:i) { #j에 x에 값을 할당
      if(i%%j==0){ #i%%j==0 일시 
        n<-n+1 #n에 +1 의 값을 할당하라 
      }
    }
    if(n==2){ #n이 2가 될시
      cat(i,"\n") #n의 카운팅 횟수가 올라가게 만든 i를 출력하라
    }
  }
}
c(10) #함수 c의 x에 해당하는 수(10)를 할당하라

#week5-5 10개의 정수를 입력받아 사분위범위를 리턴하는 함수를 만드시오 (order)
d <- function(x1,x2,x3,x4,x5,x6,x7,x8,x9,x10){ #10개의 정수를 받는 함수 d를 생성
  x <- c(x1,x2,x3,x4,x5,x6,x7,x8,x9,x10) #10개의 정수 객체들을 하나로 결합하여 x에 할당하라
  y <- order(x) #x의 데이터 순서값을 y에다 할당하라
  z <- c(x[y[1]],x[y[2]],x[y[3]],x[y[4]],x[y[5]],x[y[6]],x[y[7]],x[y[8]],x[y[9]],x[y[10]]) #y(할당된 데이터 순서값)을 x에 결합하여 Z에 할당하라(오름차순)
  w <- z[8] - z[3] #z의 8번째 값에서 3번째 값에 해당하는 차를 w에 할당하라
  return(w) #w값을 리턴하라
}
w<-d(10,12,13,14,4,3,5,6,1,99) #함수 d의 x에 해당하는 수(10,12,13,14,4,3,5,6,1,99)를 할당하라
w #w 값 출력

#week5-5-2 10개의 정수를 입력받아 사분위범위를 리턴하는 함수를 만드시오 
d <- function(x1,x2,x3,x4,x5,x6,x7,x8,x9,x10){ #10개의 정수를 받는 함수 d를 생성
  x <- c(x1,x2,x3,x4,x5,x6,x7,x8,x9,x10) #10개의 정수 객체들을 하나로 결합하여 x에 할당하라
