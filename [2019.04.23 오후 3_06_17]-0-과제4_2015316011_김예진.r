#week5-1 두수 입력받아 공약수 출력
a<-function(x,y){ #x,y를 받는 함수 a 생성 
  for(i in 1:y){ #i에 1부터 y까지의 값을 반복하여 할당하라
    if(y%%i==0 && x%%i==0){ #y를 i로 나누었을 때 0이고 x를 0으로 나누었을 때 0인 값 (두 조건을 모두 만족하는 경우가 공약수)
      print(i) #공약수 i를 모두 출력하라
    }
  }
} 
a(9,27)#함수에 x,y에 해당하는 두 수(9,27)를 할당해 줌


